//////////////////////////////////////////////////////////////////////////
/*
@brief		执行命令（关闭主数据库链接，备份数据库链接）   

@param		无

@retval     long，0：成功，非0：失败	

@exception  无
*/
//////////////////////////////////////////////////////////////////////////
long Data::CCloseCommand::ExecuteCommand()
{
	int sqlite_result = SQLITE_OK;
	sqlite_result = sqlite3_close(m_pConnection);
	sqlite_result = sqlite3_close(m_pBackupConnection);
	return sqlite_result;
};